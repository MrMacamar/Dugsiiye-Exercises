//  Array declaration

let colors = ["Blue", "Black", "White"];

console.log(colors);


// push

console.log("Push section");
colors.push("Grey");
console.log(colors);

// Pop

console.log("Push section");
colors.pop();
console.log(colors);

// Unshift 

console.log("Unshift section");
colors.unshift("Purple");
console.log(colors);


// Shift

console.log("Shift section");
colors.shift()
console.log(colors);


// Length

console.log("Length section");
console.log(colors.length);